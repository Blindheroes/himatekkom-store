    }

    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        Log::info('Registration attempt', $request->all());

        $validator = Validator::make($request->all(), [
            'name' => 'required|string|max:255',
            'email' => 'required|string|email|max:255|unique:users',
            'password' => 'required|string|min:8|confirmed',
        ]);

        if ($validator->fails()) {
            return back()
                ->withErrors($validator)
                ->withInput();
        }

        $isFirstUser = User::count() === 0;
        if ($isFirstUser) {
            $request->merge(['is_admin' => true]);
        }
        $request->merge(['is_admin' => false]);

        $user = User::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => Hash::make($request->password),
            'is_admin' => $request->is_admin,
        ]);

        Auth::login($user);

        if ($isFirstUser) {
            return redirect('/admin')->with('success', 'Registration successful! You are now logged in as an admin.');
        }
        return redirect('/seller')->with('success', 'Registration successful! You are now logged in as a seller.');
    }

